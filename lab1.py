# List Example
# Creating a list
fruits = ["Apple", "Banana", "Cherry", "Mango"]
print("Complete List:", fruits)  # Output: ['Apple', 'Banana', 'Cherry', 'Mango']

# Accessing elements
print("First fruit:", fruits[0])  # Output: Apple
print("Last fruit:", fruits[-1])  # Output: Mango

# Slicing a list
print("First two fruits:", fruits[:2])  # Output: ['Apple', 'Banana']

#  using negative indices
print("Last two fruits (-2:):", fruits[-2:])  # Output: ['Cherry', 'Mango']
print("Middle two fruits (-3:-1):", fruits[-3:-1])  # Output: ['Banana', 'Cherry']

